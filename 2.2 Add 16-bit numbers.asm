; PROGRAM TO ADD TWO 16-BIT DATA
DATA SEGMENT
    ORG 1104H
    SUM DW 0
    CARRY DB 0
DATA ENDS

CODE SEGMENT
    ASSUME CS:CODE
    ASSUME DS:DATA
    ORG 1000H
    
    MOV AX, 205AH
    MOV BX, 40EDH
    MOV CL, 00H
    ADD AX, BX
    MOV SUM,AX
    JNC AHEAD
    INC CL
AHEAD MOV CARRY, CL
    HLT
CODE ENDS
END






